package Ipoo20201003ap1;

public class Professor extends Contrato {
	private String atividadeExtra;
	private String turma;
	private String diploma;

	public String getAtividadeExtra() {
		return atividadeExtra;
	}
	public void setAtividadeExtra(String atividadeExtra) {
		this.atividadeExtra = atividadeExtra;	
	}
	
	public String getTurma() {
		return turma;
	}
	public void setTurma(String turma) {
		this.turma = turma;
	}
	
	public String getDiploma() {
		return diploma;
	}
	public void setDiploma(String diploma) {
		this.diploma = diploma;
		
	}

}
